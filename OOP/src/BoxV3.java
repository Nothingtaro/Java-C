public class BoxV3 {
    private double width, height, depth; // fields

    // Default constructor
    public BoxV3() {
    }

    public BoxV3(double width, double height, double depth) {
        this.width = width;
        this.height = height;
        this.depth = depth;
    }

    @Override
    public String toString() {
        return "BoxV3{" +
                "w=" + width +
                ", h=" + height +
                ", d=" + depth +
                '}';
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getDepth() {
        return depth;
    }

    public void setDepth(double depth) {
        this.depth = depth;
    }
}
